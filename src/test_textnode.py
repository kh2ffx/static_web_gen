class TestTextNode(unittest.TestCase):
    def test_eq(self):
        node = TextNode("This is a text node", TextType.TEXT)
        node2 = TextNode("This is a text node", TextType.TEXT)
        self.assertEqual(node, node2)

    def test_eq_false(self):
        node = TextNode("This is a text node", TextType.TEXT)
        node2 = TextNode("This is a text node", TextType.BOLD_TEXT)
        self.assertNotEqual(node, node2)

    def test_eq_false2(self):
        node = TextNode("This is a text node", TextType.TEXT)
        node2 = TextNode("This is a text node2", TextType.TEXT)
        self.assertNotEqual(node, node2)

    def test_eq_url(self):
        node = TextNode("This is a text node", TextType.ITALIC_TEXT, "https://www.boot.dev")
        node2 = TextNode("This is a text node", TextType.ITALIC_TEXT, "https://www.boot.dev")
        self.assertEqual(node, node2)

    def test_repr(self):
        node = TextNode("This is a text node", TextType.TEXT, "https://www.boot.dev")
        self.assertEqual(
            "TextNode(This is a text node, text, https://www.boot.dev)", repr(node)
            )

class TestTextNodeToHTMLNode(unittest.TestCase):
    def test_text(self):
        node = TextNode("This is a text node", TextType.TEXT)
        html_node = text_node_to_html_node(node)
        self.assertEqual(html_node.tag, None)
        self.assertEqual(html_node.value, "This is a text node")

    def test_image(self):
        node = TextNode("This is an image", TextType.IMAGE, "https://www.boot.dev")
        html_node = text_node_to_html_node(node)
        self.assertEqual(html_node.tag, "img")
        self.assertEqual(html_node.value, "")
        self.assertEqual(
            html_node.props,
            {"src": "https://www.boot.dev", "alt": "This is an image"},
        )

    def test_bold(self):
        node = TextNode("This is bold", TextType.BOLD_TEXT)
        html_node = text_node_to_html_node(node)
        self.assertEqual(html_node.tag, "b")
        self.assertEqual(html_node.value, "This is bold")


class Test_split_nodes_delimiter(unittest.TestCase):
    def test_node_delimiters(self):
        node_c = TextNode("This is text with a `code` block", TextType.TEXT)
        node_b = TextNode("This is text with a **BOLD** block", TextType.TEXT)
        node_i = TextNode("This is text with a _italic_ block", TextType.TEXT)
        test_c = split_nodes_delimiter([node_c], "`", TextType.CODE_TEXT)
        test_b = split_nodes_delimiter([node_b], "**", TextType.BOLD_TEXT)
        test_i = split_nodes_delimiter([node_i], "_", TextType.ITALIC_TEXT)
        self.assertEqual(test_c, [TextNode("This is text with a ", TextType.TEXT),
                          TextNode("code", TextType.CODE_TEXT),
                          TextNode(" block", TextType.TEXT)]
                 )
        self.assertEqual(test_b, [TextNode("This is text with a ", TextType.TEXT),
                          TextNode("BOLD", TextType.BOLD_TEXT),
                          TextNode(" block", TextType.TEXT)]
                 )
        self.assertEqual(test_i, [TextNode("This is text with a ", TextType.TEXT),
                          TextNode("italic", TextType.ITALIC_TEXT),
                          TextNode(" block", TextType.TEXT)]
                 )
